package mail;

public class EmailTO {
	
	private String mail;
	private String CetificationNumber;
	

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getCetificationNumber() {
		return CetificationNumber;
	}

	public void setCetificationNumber(String cetificationNumber) {
		CetificationNumber = cetificationNumber;
	}
	
	
}
